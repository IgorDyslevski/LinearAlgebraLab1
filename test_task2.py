def test_matrix_sum(): 
    n1, m1 = 7, 7
    matrix1 = [[9, 0, 0, 3, 1, 0, 1],
              [0, 11, 2, 1, 0, 0, 2],
              [0, 1, 10, 2, 0, 0, 0],
              [2, 1, 2, 9, 1, 0, 0],
              [1, 0, 0, 1, 12, 0, 1],
              [0, 0, 0, 0, 0, 8, 0],
              [2, 2, 0, 0, 3, 0, 8]]
    

    n2, m2 = 7, 7
    matrix2 = [[9, 0, 0, 3, 1, 0, 1],
              [0, 11, 2, 1, 0, 0, 2],
              [0, 1, 10, 2, 0, 0, 0],
              [2, 1, 2, 9, 1, 0, 0],
              [1, 0, 0, 1, 12, 0, 1],
              [0, 0, 0, 0, 0, 8, 0],
              [2, 2, 0, 0, 3, 0, 8]]
    sparse1 = MatrixUtils.dense2sparse(DenseMatrix(matrix1))
    sparse2 = MatrixUtils.dense2sparse(DenseMatrix(matrix2))
