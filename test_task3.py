def test_det():
    n, m = 7, 7
    matrix = [[9, 0, 0, 3, 1, 0, 1],
              [0, 11, 2, 1, 0, 0, 2],
              [0, 1, 10, 2, 0, 0, 0],
              [2, 1, 2, 9, 1, 0, 0],
              [1, 0, 0, 1, 12, 0, 1],
              [0, 0, 0, 0, 0, 8, 0],
              [2, 2, 0, 0, 3, 0, 8]]
    assert det(n, m, matrix) == 5184832

    n, m = 7, 7
    matrix = [[9, 0, 0, 3, 1, 0, 1],
              [0, 11, 2, 1, 0, 0, 2],
              [0, 1, 10, 2, 0, 0, 0],
              [2, 1, 2, 9, 1, 0, 0],
              [1, 0, 0, 1, 12, 0, 1],
              [0, 0, 0, 0, 0, 8, 0],
              [0, 0, 0, 0, 0, 8, 0]]
    assert det(n, m, matrix) == 0

